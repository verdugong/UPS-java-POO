    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        String ruta = txtRuta.getText();
        if (ruta != null && !ruta.trim().isEmpty()) {
                directorioControlador.actualizarRutaModelo(ruta);
                directorioControlador.actualizarModeloLista();
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese una ruta válida antes de listar", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnListarActionPerformed

    private void btnListarAOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarAOActionPerformed
        String ruta = txtRuta.getText();
        if (ruta != null && !ruta.trim().isEmpty()) {
            directorioControlador.actualizarRutaModelo(ruta);
            directorioControlador.actualizarModeloLista(true, false); 
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese una ruta válida antes de listar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnListarAOActionPerformed

