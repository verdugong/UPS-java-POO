    private void crearArchivoMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearArchivoMenuItemActionPerformed
        String[] opciones = {"Crear Archivo", "Crear Directorio"};
        int seleccion = JOptionPane.showOptionDialog(null, "Seleccione el tipo de elemento a crear:",
                "Crear Archivo o Directorio", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,
                null, opciones, opciones[0]);

        if (seleccion >= 0) {
            String nombreElemento = JOptionPane.showInputDialog("Ingrese el nombre del " + opciones[seleccion] + ":");
            if (nombreElemento != null && !nombreElemento.trim().isEmpty()) {
                  directorioControlador.crearElementoEnRuta(nombreElemento, seleccion == 1, directorio.getRutaActual());
            } else {
                JOptionPane.showMessageDialog(null, "Nombre de " + opciones[seleccion] + " inválido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_crearArchivoMenuItemActionPerformed

    private void renombrarArchivoMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renombrarArchivoMenuItemActionPerformed
        String nombreElementoActual = JOptionPane.showInputDialog("Ingrese el nombre del archivo o directorio a renombrar:");
        if (nombreElementoActual != null && !nombreElementoActual.trim().isEmpty()) {
            String nuevoNombre = JOptionPane.showInputDialog("Ingrese el nuevo nombre:");
            if (nuevoNombre != null && !nuevoNombre.trim().isEmpty()) {
                directorioControlador.renombrarElementoEnRuta(nombreElementoActual, nuevoNombre, directorio.getRutaActual());
            } else {
                JOptionPane.showMessageDialog(null, "Nuevo nombre inválido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nombre de archivo o directorio actual inválido", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_renombrarArchivoMenuItemActionPerformed

    private void eliminarArchivoMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarArchivoMenuItemActionPerformed
        String nombreElemento = JOptionPane.showInputDialog("Ingrese el nombre del archivo o directorio a eliminar:");
        if (nombreElemento != null && !nombreElemento.trim().isEmpty()) {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro de que desea eliminar " + nombreElemento + "?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                directorioControlador.eliminarElementoEnRuta(nombreElemento, directorio.getRutaActual());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nombre de archivo o directorio inválido", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_eliminarArchivoMenuItemActionPerformed

